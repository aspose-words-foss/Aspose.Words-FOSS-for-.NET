        /// Converts the HTML numeric character reference to UTF-16 characters.
        /// </summary>
        /// <param name="numericReference">HTML numeric character reference to convert.</param>
        /// <returns>A string of UTF-16 characters that correspond to the numeric character reference.</returns>
        /// <remarks>
        /// A numeric character reference is actually an UTF-32 character, but with some exceptions.
        /// </remarks>
        internal static string Convert(int numericReference)
        {
            // Null character reference, references to UTF-16 surrogate characters, and references
            // to characters outside Unicode are all considered invalid.
            bool codeIsInvalid = (numericReference == 0) ||
                !UnicodeUtil.IsValidUtf32(numericReference) ||
                UnicodeUtil.IsSurrogate(numericReference);
            if (codeIsInvalid)
            {
                // The Unicode replacement character.
                return new string(UnicodeUtil.ReplacementChar, 1);
            }

            // Numeric references from 0x80 to 0x9F inclusive are converted using the Windows-1252 encoding
            // for historical reasons.
            const int win1252FragmentFirstCode = 0x80;
            int win1252FragmentLastCode = win1252FragmentFirstCode + gWin1252EncodingFragment.Length - 1;
            if ((numericReference >= win1252FragmentFirstCode) && (numericReference <= win1252FragmentLastCode))
            {
                return gWin1252EncodingFragment[numericReference - win1252FragmentFirstCode];
            }
            else
            {
                return UnicodeUtil.ConvertFromUtf32(numericReference);
            }
        }

        /// <summary>
        /// Converts the HTML named character reference to UTF-16 characters.
        /// </summary>
        /// <param name="namedReference">HTML named character reference to convert.</param>
        /// <returns>
        /// A string of UTF-16 characters that correspond to the reference.
        /// If the name of the reference is unknown, an empty string will be returned.
        /// </returns>
        internal static string Convert(string namedReference)
        {
            return gNames.GetCharacters(namedReference);
        }

        /// <summary>
        /// Determines if there is a longer named reference whose name starts with the given characters.
        /// </summary>
        /// <param name="prefix">The characters with which a longer named reference must start.</param>
        /// <returns>
        /// <c>true</c> if a longer named reference exists;
        /// <c>false</c> otherwise.
        /// </returns>
        /// <remarks>
        /// Some named references are prefixes of other named references. According to HTML 5 rules, the longest possible
        /// named reference must be used, so we need to check whether a longer reference exists that starts with
        /// the given characters.
        /// </remarks>
        internal static bool LongerNamedReferenceExists(string prefix)
        {
            return gNames.LongerNameExists(prefix);
        }

        /// <summary>
        /// Map of HTML character reference names to UTF-16 characters.
        /// </summary>
        private static readonly HtmlCharacterReferenceNames gNames;

        /// <summary>
        /// Fragment of the Windows-1252 encoding for numeric references from 0x80 to 0x9F inclusive.
        /// </summary>
        /// <remarks>
        /// If a code is not defined in Windows-1252, Unicode code point is used instead of it.
        /// It is unlikely that such a code will be inserted in an HTML document, because it doesn't map to any character
        /// in Windows-1252 and maps to a control character in Unicode.
        /// </remarks>
        private static readonly string[] gWin1252EncodingFragment =
        {
            "\u20AC", // EURO SIGN
            "\u0081", // not defined in Windows-1252, use Unicode instead
            "\u201A", // SINGLE LOW-9 QUOTATION MARK
            "\u0192", // LATIN SMALL LETTER F WITH HOOK
            "\u201E", // DOUBLE LOW-9 QUOTATION MARK
            "\u2026", // HORIZONTAL ELLIPSIS
            "\u2020", // DAGGER
            "\u2021", // DOUBLE DAGGER
            "\u02C6", // MODIFIER LETTER CIRCUMFLEX ACCENT
            "\u2030", // PER MILLE SIGN
            "\u0160", // LATIN CAPITAL LETTER S WITH CARON
            "\u2039", // SINGLE LEFT-POINTING ANGLE QUOTATION MARK
            "\u0152", // LATIN CAPITAL LIGATURE OE
            "\u008D", // REVERSE LINE FEED - not defined in Windows-1252, use Unicode instead
            "\u017D", // LATIN CAPITAL LETTER Z WITH CARON
            "\u008F", // SINGLE SHIFT THREE - not defined in Windows-1252, use Unicode instead
            "\u0090", // DEVICE CONTROL STRING - not defined in Windows-1252, use Unicode instead
            "\u2018", // LEFT SINGLE QUOTATION MARK
            "\u2019", // RIGHT SINGLE QUOTATION MARK
            "\u201C", // LEFT DOUBLE QUOTATION MARK
            "\u201D", // RIGHT DOUBLE QUOTATION MARK
            "\u2022", // BULLET
            "\u2013", // EN DASH
            "\u2014", // EM DASH
            "\u02DC", // SMALL TILDE
            "\u2122", // TRADE MARK SIGN
            "\u0161", // LATIN SMALL LETTER S WITH CARON
            "\u203A", // SINGLE RIGHT-POINTING ANGLE QUOTATION MARK
            "\u0153", // LATIN SMALL LIGATURE OE
            "\u009D", // OPERATING SYSTEM COMMAND - not defined in Windows-1252, use Unicode instead
            "\u017E", // LATIN SMALL LETTER Z WITH CARON
            "\u0178"  // LATIN CAPITAL LETTER Y WITH DIAERESIS
        };
    }
