        internal static FixedCommandIdentifier DocxToFixedCommandIdentifier(string value)
        {
            switch (value)
            {
                // These commands can occur in allocated commands. We can do them via enum reasonably easy.
                case "ApplyStyleName": return FixedCommandIdentifier.ApplyStyleName;
                case "ApplyFontName": return FixedCommandIdentifier.ApplyFontName;
                case "ApplyAutoTextName": return FixedCommandIdentifier.ApplyAutoTextName;
                case "Columns": return FixedCommandIdentifier.Columns;
                case "Condensed": return FixedCommandIdentifier.Condensed;
                case "Expanded": return FixedCommandIdentifier.Expanded;
                case "FontSize": return FixedCommandIdentifier.FontSize;
                case "Lowered": return FixedCommandIdentifier.Lowered;
                case "Raised": return FixedCommandIdentifier.Raised;
                case "FileOpenFile": return FixedCommandIdentifier.FileOpenFile;
                case "Shading": return FixedCommandIdentifier.Shading;
                case "Borders": return FixedCommandIdentifier.Borders;
                case "Color": return FixedCommandIdentifier.Color;
                case "Symbol": return FixedCommandIdentifier.Symbol;
                // But the rest of the commands can only occur in keymap customziations which is pretty 
                // rare so the simplest I can do is just to loop through the two huge static arrays below.
                // I don't want to write any more fancier code for this purpose.
                default:
                {
                    Debug.Assert(FixedCommandNames.Length == FixedCommandIdentifiers.Length);
                    for (int i = 0; i < FixedCommandNames.Length; i++)
                    {
                        if (value == FixedCommandNames[i])
                            return (FixedCommandIdentifier)FixedCommandIdentifiers[i];
                    }
                    return FixedCommandIdentifier.None;
                }
            }
        }

