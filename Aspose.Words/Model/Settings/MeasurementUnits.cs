namespace Aspose.Words
{
    /// <summary>
    /// Specifies the unit of measurement.
    /// </summary>
    public enum MeasurementUnits
    {
        /// <summary>
        /// Inches.
        /// </summary>
        Inches = 0,

        /// <summary>
        /// Centimeters.
        /// </summary>
        Centimeters = 1,

        /// <summary>
        /// Millimeters.
        /// </summary>
        Millimeters = 2,

        /// <summary>
        /// Points.
        /// </summary>
        Points = 3,

        /// <summary>
        /// Picas (commonly used in traditional typewriter font spacing).
        /// </summary>
        Picas = 4
    }
}
