namespace Aspose.Words
{
    /// <summary>
    /// Application family or office tools used to create a document.
    /// </summary>
    internal enum ApplicationType
    {
        /// <summary>
        /// The application is unknown.
        /// </summary>
        Unknown = 0,

        /// <summary>
        /// Microsoft Office Word.
        /// </summary>
        MsWord = 1,

        /// <summary>
        /// WPS Office or WPS Writer.
        /// </summary>
        WpsOffice = 2,

        /// <summary>
        /// Open Office Writer.
        /// </summary>
        OpenOfficeWriter = 3,

        /// <summary>
        /// Microsoft Works.
        /// </summary>
        MsWorks = 4
    }
}
