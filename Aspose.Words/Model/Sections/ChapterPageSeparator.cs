namespace Aspose.Words
{
    /// <summary>
    /// Defines the separator character that appears between the chapter and page number.
    /// </summary>
    /// <seealso cref="PageSetup"/>
    /// <seealso cref="PageSetup.ChapterPageSeparator"/>
    public enum ChapterPageSeparator
    {
        /// <summary>
        /// A colon.
        /// </summary>
        Hyphen = 0,
        /// <summary>
        /// A period.
        /// </summary>
        Period = 1,
        /// <summary>
        /// A colon.
        /// </summary>
        Colon = 2,
        /// <summary>
        /// An emphasized dash.
        /// </summary>
        EmDash = 3,
        /// <summary>
        /// A standard dash.
        /// </summary>
        EnDash = 4
    }
}
