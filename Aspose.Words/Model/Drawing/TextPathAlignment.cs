namespace Aspose.Words.Drawing
{
    /// <summary>
    /// WordArt alignment.
    /// </summary>
    /// <seealso cref="TextPath.TextPathAlignment"/>
    public enum TextPathAlignment
    {
        /// <summary>
        /// Stretch each line of text to fit width.
        /// </summary>
        Stretch = 0,      
        /// <summary>
        /// Center text on width.
        /// </summary>
        Center = 1,       
        /// <summary>
        /// Left justify.
        /// </summary>
        Left = 2,         
        /// <summary>
        /// Right justify.
        /// </summary>
        Right = 3,        
        /// <summary>
        /// Spread letters out to fit width.
        /// </summary>
        LetterJustify = 4,   
        /// <summary>
        /// Spread words out to fit width.
        /// </summary>
        WordJustify = 5
    }
}
