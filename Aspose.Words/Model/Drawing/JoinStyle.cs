namespace Aspose.Words.Drawing
{
    /// <summary>
    /// Line join style.
    /// </summary>
    /// <seealso cref="Stroke.JoinStyle"/>
    public enum JoinStyle
    {
        /// <summary>
        /// Join edges by a straight line.
        /// </summary>
        Bevel = 0,     
        /// <summary>
        /// Extend edges until they join.
        /// </summary>
        Miter = 1,     
        /// <summary>
        /// Draw an arc between the two edges.
        /// </summary>
        Round = 2      
    }
}
