namespace Aspose.Words.Drawing
{
    /// <summary>
    /// Specifies line cap style.
    /// </summary>
    /// <seealso cref="Stroke.EndCap"/>
    public enum EndCap
    {
        /// <summary>
        /// Rounded ends.
        /// </summary>
        Round = 0,
        /// <summary>
        /// Square protrudes by half line width.
        /// </summary>
        Square = 1,
        /// <summary>
        /// Line ends at end point.
        /// </summary>
        Flat = 2,
        /// <summary>
        /// Default value is <see cref="Flat"/>.
        /// </summary>
        Default = Flat
    }
}
