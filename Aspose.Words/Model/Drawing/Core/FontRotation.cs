namespace Aspose.Words.Drawing.Core
{
    /// <summary>
    /// Specifies font rotation in a textbox. Have not seen in MS Word documents.
    /// </summary>
    /// <dev>
    /// Do not renumber. The values are from the RTF spec.
    /// </dev>
    internal enum FontRotation
    {
        /// <summary>
        /// 0 degrees rotation.
        /// </summary>
        Right = 0,
        /// <summary>
        /// 90 degrees rotation.
        /// </summary>
        Down = 1,
        /// <summary>
        /// 180 degrees rotation.
        /// </summary>
        Left = 2,
        /// <summary>
        /// 270 degrees rotation.
        /// </summary>
        Up = 3
    }
}
