namespace Aspose.Words.Drawing
{
    /// <summary>
    /// Width of the arrow at the end of a line.
    /// </summary>
    /// <seealso cref="Stroke.StartArrowWidth"/>
    /// <seealso cref="Stroke.EndArrowWidth"/>
    public enum ArrowWidth
    {
        /// <summary>
        /// </summary>
        Narrow = 0,
        /// <summary>
        /// </summary>
        Medium = 1,
        /// <summary>
        /// </summary>
        Wide = 2,

        /// <summary>
        /// Same as <see cref="Narrow"/>.
        /// </summary>
        Default = Narrow
    }
}
