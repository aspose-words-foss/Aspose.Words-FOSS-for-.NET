namespace Aspose.Words.Drawing
{
    /// <summary>
    /// Dashed line style.
    /// </summary>
    /// <seealso cref="Stroke.DashStyle"/>
    public enum DashStyle
    {
        /// <summary>
        /// Solid (continuous) pen.
        /// </summary>
        Solid = 0,
        /// <summary>
        /// System dash style.
        /// </summary>
        ShortDash = 1,
        /// <summary>
        /// System dash style.
        /// </summary>
        ShortDot = 2,
        /// <summary>
        /// System dash style.
        /// </summary>
        ShortDashDot = 3,
        /// <summary>
        /// System dash style.
        /// </summary>
        ShortDashDotDot = 4,
        /// <summary>
        /// Square dot style.
        /// </summary>
        Dot = 5,
        /// <summary>
        /// Dash style.
        /// </summary>
        Dash = 6,
        /// <summary>
        /// Long dash style.
        /// </summary>
        LongDash = 7,
        /// <summary>
        /// Dash short dash.
        /// </summary>
        DashDot = 8,
        /// <summary>
        /// Long dash short dash.
        /// </summary>
        LongDashDot = 9,
        /// <summary>
        /// Long dash short dash short dash.
        /// </summary>
        LongDashDotDot = 10,

        /// <summary>
        /// Same as <see cref="Solid"/>.
        /// </summary>
        Default = Solid
    }
}
