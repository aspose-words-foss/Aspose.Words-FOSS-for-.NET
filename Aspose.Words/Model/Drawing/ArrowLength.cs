namespace Aspose.Words.Drawing
{
    /// <summary>
    /// Length of the arrow at the end of a line.
    /// </summary>
    /// <seealso cref="Stroke.StartArrowLength"/>
    /// <seealso cref="Stroke.EndArrowLength"/>
    public enum ArrowLength
    {
        /// <summary>
        /// </summary>
        Short = 0,
        /// <summary>
        /// </summary>
        Medium = 1,
        /// <summary>
        /// </summary>
        Long = 2,

        /// <summary>
        /// Same as <see cref="Short"/>.
        /// </summary>
        Default = Short
    }
}
