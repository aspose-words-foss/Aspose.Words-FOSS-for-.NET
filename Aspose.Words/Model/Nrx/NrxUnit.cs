namespace Aspose.Words.Nrx
{
    /// <summary>
    /// Allows to specify the measurement units for model values in AW.
    /// </summary>
    internal enum NrxUnit
    {
        /// <summary>
        /// Unit not defined or not applicable
        /// </summary>
        None,
    
        /// <summary>
        /// Pica, a typographic unit of measure corresponding to 12 pt
        /// </summary>
        Pica,
    
        /// <summary>
        /// Typographic Point 1/72th of an inch
        /// </summary>
        Point,
        
        /// <summary>
        /// Imperial inch, equal 2.54 cm
        /// </summary>
        Inch,
        
        /// <summary>
        /// metric millimeter, 1/10 of cm
        /// </summary>
        Millimeter,
        
        /// <summary>
        /// Metric centimeter
        /// </summary>
        Centimeter,
        
        /// <summary>
        /// 1/2 of a point
        /// </summary>
        HalfPoints,
        
        /// <summary>
        /// 1/20 of a point
        /// </summary>
        Twips,

        /// <summary>
        /// 1/100 of a point
        /// </summary>
        HundredthsOfPoint,

        /// <summary>
        /// English Metric Units, 1/12700 of a point
        /// </summary>
        Emus
    }
}

