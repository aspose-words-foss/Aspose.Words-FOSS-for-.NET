namespace Aspose.Words.Nrx
{
    internal static class NrxNamespaces
    {
        /// <summary>
        /// "o". Common Properties. Shared office schema. Document properties collection (author, createdate, etc.) and document statistics (word count, etc.) 
        /// </summary>
        internal const string Office = "urn:schemas-microsoft-com:office:office";

        /// <summary>
        /// "v". Vector Markup Language. VML is an application of Extensible Markup Language (XML) 1.0 which defines a format for the encoding of vector 
        /// information together with additional markup to describe how that information may be displayed and edited.
        /// </summary>
        internal const string Vml = "urn:schemas-microsoft-com:vml";

        /// <summary>
        /// "w10". Auxiliary XML Document 2002. Supplemental XML information that serves as "hints" for transformation of a Word document to HTML format.
        /// For example, font, color, border, and absolute positioning information.
        /// </summary>
        internal const string Word = "urn:schemas-microsoft-com:office:word";

        internal const string Math = "http://schemas.openxmlformats.org/officeDocument/2006/math";
    }
}
