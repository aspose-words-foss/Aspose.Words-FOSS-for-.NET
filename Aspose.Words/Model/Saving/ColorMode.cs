namespace Aspose.Words.Saving
{
    /// <summary>
    /// Specifies how colors are rendered.
    /// </summary>
    public enum ColorMode
    {
        /// <summary>
        /// Rendering with unmodified colors.
        /// </summary>
        Normal = 0,
        /// <summary>
        /// Rendering with colors in a range of gray shades from white to black.
        /// </summary>
        Grayscale = 1
    }
}
