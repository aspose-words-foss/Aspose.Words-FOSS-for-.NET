namespace Aspose.Words.Saving
{
    /// <summary>
    /// Specifies the parity-based page selection rule.
    /// </summary>
    internal enum PageParity
    {
        /// <summary>
        /// Select pages with any parity.
        /// </summary>
        Any,

        /// <summary>
        /// Select all even pages.
        /// </summary>
        Even,

        /// <summary>
        /// Select all odd pages.
        /// </summary>
        Odd
    }
}
