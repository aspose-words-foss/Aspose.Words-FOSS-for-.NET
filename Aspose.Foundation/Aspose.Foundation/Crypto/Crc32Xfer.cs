        /// <summary>
        /// Compute a CRC-32/XFER checksum for a given array of bytes.
        /// </summary>
        /// <param name="bytes">The array of bytes to compute the checksum for.</param>
        /// <returns>The computed checksum as an unsigned integer.</returns>
        public static uint GetCrc(byte[] bytes)
        {
            uint current = 0;

            for (int i = 0; i < bytes.Length; i++)
            {
                current = gPrecomputationTable[((current >> 24) ^ bytes[i]) & 0xFF] ^ (current << 8);
            }

            return current;
        }

        /// <summary>
        /// Compute a CRC-32/XFER checksum for a given array of bytes.
        /// </summary>
        /// <param name="bytes">The array of bytes to compute the checksum for.</param>
        /// <returns>The computed checksum as byte array.</returns>
        public static byte[] GetBytes(byte[] bytes)
        {
            return BitConverter.GetBytes(GetCrc(bytes));
        }

        /// <summary>
        /// Compute a CRC-32/XFER checksum for a given array of bytes.
        /// </summary>
        /// <param name="bytes">The array of bytes to compute the checksum for.</param>
        /// <returns>The computed checksum as base64 string.</returns>
        public static string GetBase64String(byte[] bytes)
        {
            return Convert.ToBase64String(GetBytes(bytes));
        }
