namespace Aspose.Common
{
    /// <summary>
    /// Names of XML namespaces that are used in formats developed by World Wide Web Consortium (W3C).
    /// </summary>
    public static class W3CNamespaces
    {
        /// <summary>
        /// Namespace name that is bound to the "xml" prefix.
        /// </summary>
        public const string Xml = "http://www.w3.org/XML/1998/namespace";

        /// <summary>
        /// The XHTML namespace name, which is used for all HTML elements.
        /// </summary>
        public const string Xhtml = "http://www.w3.org/1999/xhtml";

        public const string MathML = "http://www.w3.org/1998/Math/MathML";

        public const string Svg = "http://www.w3.org/2000/svg";

        public const string XLink = "http://www.w3.org/1999/xlink";
    }
}
