        public static void ProcessArabicSimplified(TTGlyphHashtable glyphs)
        {
            // Actually we do not have fonts with this encoding available. 
            // Tables from the spec should probably work fine but misc dictionary may not be fully relevant.
            ProcessArabicTable(glyphs, 0xF100, gSimpleArabicIsolate, LetterForm.Isolated);
            ProcessArabicTable(glyphs, 0xF100, gSimpleArabicInitial, LetterForm.Initial);
            ProcessArabicTable(glyphs, 0xF100, gSimpleArabicMedial, LetterForm.Medial);
            ProcessArabicTable(glyphs, 0xF100, gSimpleArabicFinal, LetterForm.Final);
            ProcessDictionary(glyphs, 0xF100, gArabicMisc);
        }

        public static void ProcessArabicTraditional(TTGlyphHashtable glyphs)
        {
            ProcessArabicTable(glyphs, 0xF200, gTraditionalArabicIsolate, LetterForm.Isolated);
            ProcessArabicTable(glyphs, 0xF200, gTraditionalArabicInitial, LetterForm.Initial);
            ProcessArabicTable(glyphs, 0xF200, gTraditionalArabicMedial, LetterForm.Medial);
            ProcessArabicTable(glyphs, 0xF200, gTraditionalArabicFinal, LetterForm.Final);
            ProcessDictionary(glyphs, 0xF200, gArabicMisc);
        }

        public static void ProcessHebrew(TTGlyphHashtable glyphs)
        {
            ProcessTable(glyphs, 0xF020, gHebrew);
        }

        private static void ProcessDictionary(TTGlyphHashtable glyphs, int encodingBase, IntToIntDictionary dic)
        {
            IntToIntDictionary.Enumerator enumerator =  dic.GetEnumerator();
            while (enumerator.MoveNext())
            {
                int unicodeChar = enumerator.CurrentKey;
                int encodingChar = enumerator.CurrentValue + encodingBase;
                ProcessChar(glyphs, encodingChar, unicodeChar);
            }
        }
        
        private static void ProcessTable(TTGlyphHashtable glyphs, int encodingBase, int[] table)
        {
            for (int i = 0; i < table.Length; i++)
            {
                int encodingChar = i + encodingBase;
                int unicodeChar = table[i];
                if (unicodeChar != 0)
                    ProcessChar(glyphs, encodingChar, unicodeChar);
            }
        }

        private static void ProcessArabicTable(TTGlyphHashtable glyphs, int encodingBase, int[] table,
            LetterForm letterForm)
        {
            const int unicodeBase = 0x620;
            for (int i = 0; i < table.Length; i++)
            {
                int unicodeChar = unicodeBase + i;
                int encodingChar = encodingBase + table[i];
                if (table[i] != 0)
                    ProcessArabicChar(glyphs, encodingChar, unicodeChar, letterForm);
            }
        }

        private static void ProcessArabicChar(TTGlyphHashtable glyphs, int encodingChar, int unicodeChar, LetterForm letterForm)
        {
            if(letterForm == LetterForm.Isolated)
                ProcessChar(glyphs, encodingChar, unicodeChar);
            
            int presentationForm = UnicodeArabicShapingResolver.GetArabicCharacterByLetterForm((char)unicodeChar, letterForm);
            if(presentationForm != unicodeChar)
                ProcessChar(glyphs, encodingChar, presentationForm);
        }

        private static void ProcessChar(TTGlyphHashtable glyphs, int encodingChar, int unicodeChar)
        {
            TTGlyph glyph = glyphs.GetGlyphByCharCode(encodingChar);
            if(glyph == null)
                return;
            
            glyphs.AddCharCodeMapping(unicodeChar, glyph.GlyphIndex);
        }

