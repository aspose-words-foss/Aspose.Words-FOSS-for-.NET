        #region AW extends

        /// <summary>
        /// Returns the index of the specified character excluding any literals enclosed in single quotes.
        /// </summary>
        private static int IndexOfExcludingLiterals(IString s, char c)
        {
            int index = 0;

            LiteralsAwareCharEnumerator enumerator = new LiteralsAwareCharEnumerator(s, true);
            while (enumerator.MoveNext())
            {
                if (c == enumerator.Current.ToSystemChar())
                    return index;

                index++;
            }

            return -1;
        }

        /// <summary>
        /// Replaces a character in the string with another one skipping literals enclosed in single quotes.
        /// </summary>
        private static IString ReplaceExcludingLiterals(IString s, char oldChar, char newChar, IStringBehaviour stringBehaviour)
        {
            IStringBuilder builder = stringBehaviour.CreateBuilder();

            LiteralsAwareCharEnumerator enumerator = new LiteralsAwareCharEnumerator(s, false);
            while (enumerator.MoveNext())
            {
                if (!enumerator.IsInLiteral && enumerator.Current.ToSystemChar() == oldChar)
                    builder.Append(newChar, enumerator.Current);
                else
                    builder.Append(enumerator.Current);
            }

            return builder.ToIString();
        }

        /// <summary>
        /// Enumerates over string characters being aware of string literals enclosed in single quotes.
        /// </summary>
        private class LiteralsAwareCharEnumerator
        {
            public LiteralsAwareCharEnumerator(IString s, bool skipLiterals)
            {
                mCharEnumerator = s;
                mCharLength = s.Length;
                // Enumerators are positioned before the first element
                // until the first MoveNext() call.
                mCharIndex = -1;
                mSkipLiterals = skipLiterals;
            }

            public bool MoveNext()
            {
                if (!DoMoveNext())
                    return false;

                if (mSkipLiterals)
                {
                    while (IsInLiteral)
                    {
                        if (!DoMoveNext())
                            return false;
                    }
                }

                return true;
            }

            private bool DoMoveNext()
            {
                mCharIndex++;
                if (mCharIndex >= mCharLength)
                    return false;

                if (Current.ToSystemChar() == '\'')
                    IsInLiteral = !IsInLiteral;

                return true;
            }

            public IChar Current
            {
                get { return mCharEnumerator[mCharIndex]; }
            }

            public bool IsInLiteral { get; private set; }

            private readonly bool mSkipLiterals;
            private int mCharIndex;
            private readonly int mCharLength;
            private readonly IString mCharEnumerator;
        }

        #endregion
    }
