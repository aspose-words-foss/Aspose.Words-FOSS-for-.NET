        private void AppendIntegerString(int minLength, List<IChar> sources, IStringBuilder sb)
        {
            if (this.mDecPointPos <= 0)
            {
                // minLength always equals 1:
                // minLength equals IntegerDigits (only one method usage)
                // IntegerDigits returns 1 when mDecPointPos <= 0
                Debug.Assert(minLength == 1);
                IChar source = (sources != null) && (sources.Count > 0)
                    ? sources[sources.Count - 1]
                    : null;
                sb.Append('0', minLength, source);
                return;
            }

            if (this.mDecPointPos < minLength)
            {
                // minLength always equals mDecPointPos:
                // minLength = IntegerDigits (only one method usage)
                // IntegerDigits returns mDecPointPos when mDecPointPos > 0
                Debug.Fail("Unexpected");
                sb.Append('0', minLength - this.mDecPointPos, null);
            }

            AppendDigits(this.mDigitsLen - this.mDecPointPos, this.mDigitsLen, sources, sb);
        }

        private void AppendDecimalString(int precision, List<IChar> sources, IStringBuilder sb)
        {
            AppendDigits(this.mDigitsLen - precision - this.mDecPointPos, this.mDigitsLen - this.mDecPointPos, sources, sb);
        }

        // minDigits is in the range 1..3
        private void AppendExponent(NumberFormatInfo nfi, int exponent, int minDigits)
        {
            if (this.mSpecifierIsUpper || this.mSpecifier == 'R')
            {
                Append('E');
            }
            else
            {
                Append('e');
            }

            if (exponent >= 0)
            {
                Append(nfi.PositiveSign);
            }
            else
            {
                Append(nfi.NegativeSign);
                exponent = -exponent;
            }

            if (exponent == 0)
            {
                Append('0', minDigits);
            }
            else if (exponent < 10)
            {
                Append('0', minDigits - 1);
                Append((char)('0' | exponent));
            }
            else
            {
                uint hexDigit = FastToDecHex(exponent);
                if (exponent >= 100 || minDigits == 3)
                {
                    Append((char)('0' | (hexDigit >> 8)));
                }
                Append((char)('0' | ((hexDigit >> 4) & 0xf)));
                Append((char)('0' | (hexDigit & 0xf)));
            }
        }

        private void AppendOneDigit(int start)
        {
            if (this.mInd == this.mCbuf.Length)
            {
                Resize(this.mInd + 10);
            }

            start += this.mOffset;
            uint v;
            if (start < 0)
            {
                v = 0;
            }
            else if (start < 8)
            {
                v = this.mVal1;
            }
            else if (start < 16)
            {
                v = this.mVal2;
            }
            else if (start < 24)
            {
                v = this.mVal3;
            }
            else if (start < 32)
            {
                v = this.mVal4;
            }
            else
            {
                v = 0;
            }
            v >>= (start & 0x7) << 2;
            this.mCbuf[this.mInd++] = (char)('0' | v & 0xf);
        }

        private void AppendDigits(int start, int end)
        {
            if (start >= end)
            {
                return;
            }

            int i = this.mInd + (end - start);
            if (i > this.mCbuf.Length)
            {
                Resize(i + 10);
            }
            this.mInd = i;

            end += this.mOffset;
            start += this.mOffset;

            for (int next = start + 8 - (start & 0x7);; start = next, next += 8)
            {
                uint v;
                if (next == 8)
                {
                    v = this.mVal1;
                }
                else if (next == 16)
                {
                    v = this.mVal2;
                }
                else if (next == 24)
                {
                    v = this.mVal3;
                }
                else if (next == 32)
                {
                    v = this.mVal4;
                }
                else
                {
                    v = 0;
                }
                v >>= (start & 0x7) << 2;
                if (next > end)
                {
                    next = end;
                }

                this.mCbuf[--i] = (char)('0' | v & 0xf);
                switch (next - start)
                {
                    case 8:
                        this.mCbuf[--i] = (char)('0' | (v >>= 4) & 0xf);
                        goto case 7;
                    case 7:
                        this.mCbuf[--i] = (char)('0' | (v >>= 4) & 0xf);
                        goto case 6;
                    case 6:
                        this.mCbuf[--i] = (char)('0' | (v >>= 4) & 0xf);
                        goto case 5;
                    case 5:
                        this.mCbuf[--i] = (char)('0' | (v >>= 4) & 0xf);
                        goto case 4;
                    case 4:
                        this.mCbuf[--i] = (char)('0' | (v >>= 4) & 0xf);
                        goto case 3;
                    case 3:
                        this.mCbuf[--i] = (char)('0' | (v >>= 4) & 0xf);
                        goto case 2;
                    case 2:
                        this.mCbuf[--i] = (char)('0' | (v >> 4) & 0xf);
                        goto case 1;
                    case 1:
                        if (next == end)
                        {
                            return;
                        }
                        continue;
                }
            }
        }

        private void AppendDigits(int start, int end, List<IChar> sources, IStringBuilder sb)
        {
            if (start >= end)
            {
                return;
            }

            int i = sb.Length + (end - start);
            sb.Length = i;

            end += this.mOffset;
            start += this.mOffset;

            for (int next = start + 8 - (start & 0x7);; start = next, next += 8)
            {
                uint v;
                if (next == 8)
                {
                    v = this.mVal1;
                }
                else if (next == 16)
                {
                    v = this.mVal2;
                }
                else if (next == 24)
                {
                    v = this.mVal3;
                }
                else if (next == 32)
                {
                    v = this.mVal4;
                }
                else
                {
                    v = 0;
                }
                v >>= (start & 0x7) << 2;
                if (next > end)
                {
                    next = end;
                }

                --i;
                sb.Replace(i, (char)('0' | v & 0xf), ResolveSourceChar(sources, i, sb.Length));
                switch (next - start)
                {
                    case 8:
                        --i;
                        sb.Replace(i, (char)('0' | (v >>= 4) & 0xf), ResolveSourceChar(sources, i, sb.Length));
                        goto case 7;
                    case 7:
                        --i;
                        sb.Replace(i, (char)('0' | (v >>= 4) & 0xf), ResolveSourceChar(sources, i, sb.Length));
                        goto case 6;
                    case 6:
                        --i;
                        sb.Replace(i, (char)('0' | (v >>= 4) & 0xf), ResolveSourceChar(sources, i, sb.Length));
                        goto case 5;
                    case 5:
                        --i;
                        sb.Replace(i, (char)('0' | (v >>= 4) & 0xf), ResolveSourceChar(sources, i, sb.Length));
                        goto case 4;
                    case 4:
                        --i;
                        sb.Replace(i, (char)('0' | (v >>= 4) & 0xf), ResolveSourceChar(sources, i, sb.Length));
                        goto case 3;
                    case 3:
                        --i;
                        sb.Replace(i, (char)('0' | (v >>= 4) & 0xf), ResolveSourceChar(sources, i, sb.Length));
                        goto case 2;
                    case 2:
                        --i;
                        sb.Replace(i, (char)('0' | (v >> 4) & 0xf), ResolveSourceChar(sources, i, sb.Length));
                        goto case 1;
                    case 1:
                        if (next == end)
                        {
                            return;
                        }
                        continue;
                }
            }
        }

        private static IChar ResolveSourceChar(List<IChar> sources, int index, int length)
        {
            if (sources == null || sources.Count == 0)
                return null;

            index -= length - sources.Count;

            if (index < 0)
                index = 0;

            if (index >= sources.Count)
                return null;

            return sources[index];
        }

