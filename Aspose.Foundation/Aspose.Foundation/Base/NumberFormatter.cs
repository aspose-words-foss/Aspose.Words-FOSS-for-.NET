        private static void ZeroTrimEnd(IStringBuilder sb, bool canEmpty)
        {
            int len = 0;
            for (int i = sb.Length - 1; (canEmpty ? i >= 0 : i > 0); i--)
            {
                if (sb[i].ToSystemChar() != '0')
                {
                    break;
                }
                len++;
            }

            if (len > 0)
            {
                sb.Remove(sb.Length - len, len);
            }
        }

        private static bool IsZeroOnly(IStringBuilder sb)
        {
            for (int i = 0; i < sb.Length; i++)
            {
                char c = sb[i].ToSystemChar();
                if (char.IsDigit(c) && c != '0')
                {
                    return false;
                }
            }
            return true;
        }

