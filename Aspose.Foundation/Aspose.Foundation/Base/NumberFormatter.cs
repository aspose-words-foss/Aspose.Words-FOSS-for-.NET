            IStringBehaviour stringBehaviour)
        {
            bool dropOption = HasFormatDropOption(format);
            format = ReplaceExcludingLiterals(
                format,
                'x',
                '#',
                stringBehaviour);

            NumberFormatter inst = GetInstance();
            inst.Init(format, value, DoubleDefPrecision, stringBehaviour);
            NumberFormatInfo nfi = inst.GetNumberFormatInstance(fp);
            IString res;
            if (inst.mNaN)
            {
                res = stringBehaviour.CreateString(nfi.NaNSymbol);
            }
            else if (inst.mInfinity)
            {
                if (inst.mPositive)
                {
                    res = stringBehaviour.CreateString(nfi.PositiveInfinitySymbol);
                }
                else
                {
                    res = stringBehaviour.CreateString(nfi.NegativeInfinitySymbol);
                }
            }
            else if (inst.mSpecifier == 'R')
            {
                res = inst.FormatRoundtrip(value, nfi, stringBehaviour);
            }
            else
            {
                res = inst.NumberToString(format, nfi, options, stringBehaviour);
            }
            inst.Release();

            // WORDSNET-11445 Support drop option.
            if (dropOption)
            {
                if (format[0].ToSystemChar() != '-' && format[0].ToSystemChar() != '+' && value < 0)
                    res = res.Remove(0, 1);

                int digitsNumber = format.ToSystemString().Split('#').Length - 1;
                int digitCount = 0;
                int currentCount = format.Length - 1;
                IChar[] tmp = new IChar[format.Length];
                for (int i = res.Length - 1; i >= 0; i--)
                {
                    if (digitCount < digitsNumber || !char.IsDigit(res[i].ToSystemChar()))
                    {
                        tmp[currentCount] = res[i];
                        currentCount--;
                        if (char.IsDigit(res[i].ToSystemChar()))
                            digitCount++;
                    }
                }

                IStringBuilder builder = stringBehaviour.CreateBuilder();
                foreach (IChar c in tmp)
                {
                    if (c != null)
                        builder.Append(c);
                }

                res = builder.ToIString();
            }

            return res;
        }

        /// <summary>
        /// Returns true if format has drop option (x).
        /// Drops digits to the left of the "x" placeholder.
        /// If the placeholder is to the right of the decimal point, Word rounds the result to that place.
        /// </summary>
        private static bool HasFormatDropOption(IString format)
        {
            int dropOptionIndex = IndexOfExcludingLiterals(format, 'x');
            if (dropOptionIndex != -1)
            {
                // WORDSNET-19482 Other placeholders to the left of the drop option reset it.
                int placeholder1Index = IndexOfExcludingLiterals(format, '#');
                if ((placeholder1Index != -1) && (placeholder1Index < dropOptionIndex))
                    return false;

                int placeholder2Index = IndexOfExcludingLiterals(format, '0');
                if ((placeholder2Index != -1) && (placeholder2Index < dropOptionIndex))
                    return false;

                int decimalPointIndex = IndexOfExcludingLiterals(format, '.');
                if (decimalPointIndex != -1)
                    return dropOptionIndex < decimalPointIndex;

                return true;
            }

            return false;
        }

        private IString NumberToString(
            IString format,
            NumberFormatInfo nfi,
            NumberFormattingOptions options,
            IStringBehaviour stringBehaviour)
        {
            switch (this.mSpecifier)
            {
                case 'G':
                    return FormatGeneral(this.mPrecision, nfi, stringBehaviour);

                default:
                    if (this.mIsCustomFormat)
                    {
                        return FormatCustom(format, nfi, options, stringBehaviour);
                    }
                    throw new FormatException("The specified format '" + format.ToSystemString() + "' is invalid");
            }
        }

        private IString FormatRoundtrip(double origval, NumberFormatInfo nfi, IStringBehaviour stringBehaviour)
        {
            NumberFormatter nfc = GetClone();
            if (origval >= MinRoundtripVal && origval <= MaxRoundtripVal)
            {
                IString shortRep = FormatGeneral(this.mDefPrecision, nfi, stringBehaviour);
                if (origval == Double.Parse(shortRep.ToSystemString(), nfi))
                {
                    return shortRep;
                }
            }
            return nfc.FormatGeneral(this.mDefPrecision + 2, nfi, stringBehaviour);
        }

        private IString FormatGeneral(int precision, NumberFormatInfo nfi, IStringBehaviour stringBehaviour)
        {
            bool enableExp;
            if (precision == -1)
            {
                enableExp = IsFloatingSource;
                precision = this.mDefPrecision;
            }
            else
            {
                enableExp = true;
                if (precision == 0)
                {
                    precision = this.mDefPrecision;
                }
                RoundPos(precision);
            }

            int intDigits = this.mDecPointPos;
            int digits = this.mDigitsLen;
            int decDigits = digits - intDigits;

            if ((intDigits > precision || intDigits <= -4) && enableExp)
            {
                return stringBehaviour.CreateString(FormatExponential(digits - 1, nfi, 2));
            }

            if (decDigits < 0)
            {
                decDigits = 0;
            }
            if (intDigits < 0)
            {
                intDigits = 0;
            }
            ResetCharBuf(decDigits + intDigits + 3);

            if (!this.mPositive)
            {
                Append(nfi.NegativeSign);
            }

            if (intDigits == 0)
            {
                Append('0');
            }
            else
            {
                AppendDigits(digits - intDigits, digits);
            }

            if (decDigits > 0)
            {
                Append(nfi.NumberDecimalSeparator);
                AppendDigits(0, decDigits);
            }

            return stringBehaviour.CreateString(new string(this.mCbuf, 0, this.mInd));
        }

        private string FormatExponential(int precision, NumberFormatInfo nfi, int expDigits)
        {
            int digits = this.mDigitsLen;
            int exponent = this.mDecPointPos - 1;
            this.mDecPointPos = 1;

            ResetCharBuf(precision + 8);

            if (!this.mPositive)
            {
                Append(nfi.NegativeSign);
            }

            AppendOneDigit(digits - 1);

            if (precision > 0)
            {
                Append(nfi.NumberDecimalSeparator);
                AppendDigits(digits - precision - 1, digits - this.mDecPointPos);
            }

            AppendExponent(nfi, exponent, expDigits);

            return new string(this.mCbuf, 0, this.mInd);
        }

        private IString FormatCustom(
            IString format,
            NumberFormatInfo nfi,
            NumberFormattingOptions options,
            IStringBehaviour stringBehaviour)
        {
            bool p = this.mPositive;
            int offset = 0;
            int length = 0;
            CustomInfo.GetActiveSection(format.ToSystemString(), ref p, IsZero, ref offset, ref length);
            if (length == 0)
            {
                return this.mPositive ? stringBehaviour.EmptyString : stringBehaviour.CreateString(nfi.NegativeSign);
            }
            this.mPositive = p;

            CustomInfo info = CustomInfo.Parse(format, offset, length, options, stringBehaviour);
            IStringBuilder sbInt = stringBehaviour.CreateBuilder(info.IntegerDigits * 2);
            IStringBuilder sbDec = stringBehaviour.CreateBuilder(info.DecimalDigits * 2);

            if (info.Percents > 0)
            {
                Multiply10(2 * info.Percents);
            }
            if (info.Permilles > 0)
            {
                Multiply10(3 * info.Permilles);
            }

            if (info.DividePlaces > 0)
            {
                Divide10(info.DividePlaces);
            }

            RoundDecimal(info.DecimalDigits);

            if (info.IntegerDigits != 0)
            {
                AppendIntegerString(IntegerDigits, info.IntegerDigitSources, sbInt);
            }

            AppendDecimalString(DecimalDigits, info.DecimalDigitSources, sbDec);

            if (sbInt.Length < info.IntegerDigits - info.IntegerHeadSharpDigits)
            {
                sbInt.Insert(0, "0", info.IntegerDigits - info.IntegerHeadSharpDigits - sbInt.Length, info.IntegerDigitSources);
            }
            if (info.IntegerDigits == info.IntegerHeadSharpDigits && IsZeroOnly(sbInt))
            {
                sbInt.Remove(0, sbInt.Length);
            }

            ZeroTrimEnd(sbDec, true);
            while (sbDec.Length < info.DecimalDigits - info.DecimalTailSharpDigits)
            {
                IChar source = sbDec.Length < info.DecimalDigitSources.Count
                    ? info.DecimalDigitSources[sbDec.Length]
                    : null;
                sbDec.Append('0', source);
            }
            if (sbDec.Length > info.DecimalDigits)
            {
                sbDec.Remove(info.DecimalDigits, sbDec.Length - info.DecimalDigits);
            }

            return info.Format(format, offset, length, nfi, this.mPositive, this.IsZero, sbInt, sbDec, mValue);
        }

