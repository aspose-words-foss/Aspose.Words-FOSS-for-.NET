        private void RoundBits(int shift)
        {
            if (shift <= 0)
            {
                return;
            }

            if (shift > this.mDigitsLen)
            {
                this.mDigitsLen = 0;
                this.mDecPointPos = 1;
                this.mVal1 = this.mVal2 = this.mVal3 = this.mVal4 = 0;
                this.mPositive = true;
                return;
            }
            shift += this.mOffset;
            this.mDigitsLen += this.mOffset;
            while (shift > 8)
            {
                this.mVal1 = this.mVal2;
                this.mVal2 = this.mVal3;
                this.mVal3 = this.mVal4;
                this.mVal4 = 0;
                this.mDigitsLen -= 8;
                shift -= 8;
            }
            shift = (shift - 1) << 2;
            uint v = this.mVal1 >> shift;
            uint rem16 = v & 0xf;
            this.mVal1 = (v ^ rem16) << shift;

            if (rem16 >= 0x5)
            {
                this.mVal1 |= 0x99999999 >> (28 - shift);
                AddOneToDecHex();
                int newlen = DecHexLen();
                this.mDecPointPos = this.mDecPointPos + newlen - this.mDigitsLen;
                this.mDigitsLen = newlen;
            }
            RemoveTrailingZeros();
        }

        private void RemoveTrailingZeros()
        {
            this.mOffset = CountTrailingZeros();
            this.mDigitsLen -= this.mOffset;
            if (this.mDigitsLen == 0)
            {
                this.mOffset = 0;
                this.mDecPointPos = 1;
                this.mPositive = true;
            }
        }

        private void AddOneToDecHex()
        {
            if (this.mVal1 == 0x99999999)
            {
                this.mVal1 = 0;
                if (this.mVal2 == 0x99999999)
                {
                    this.mVal2 = 0;
                    if (this.mVal3 == 0x99999999)
                    {
                        this.mVal3 = 0;
                        this.mVal4 = AddOneToDecHex(this.mVal4);
                    }
                    else
                    {
                        this.mVal3 = AddOneToDecHex(this.mVal3);
                    }
                }
                else
                {
                    this.mVal2 = AddOneToDecHex(this.mVal2);
                }
            }
            else
            {
                this.mVal1 = AddOneToDecHex(this.mVal1);
            }
        }

        // Assume val != 0x99999999
        private static uint AddOneToDecHex(uint val)
        {
            if ((val & 0xffff) == 0x9999)
            {
                if ((val & 0xffffff) == 0x999999)
                {
                    if ((val & 0xfffffff) == 0x9999999)
                    {
                        return val + 0x06666667;
                    }
                    return val + 0x00666667;
                }
                if ((val & 0xfffff) == 0x99999)
                {
                    return val + 0x00066667;
                }
                return val + 0x00006667;
            }
            if ((val & 0xff) == 0x99)
            {
                if ((val & 0xfff) == 0x999)
                {
                    return val + 0x00000667;
                }
                return val + 0x00000067;
            }
            if ((val & 0xf) == 0x9)
            {
                return val + 0x00000007;
            }
            return val + 1;
        }

        private int CountTrailingZeros()
        {
            if (this.mVal1 != 0)
            {
                return CountTrailingZeros(this.mVal1);
            }
            if (this.mVal2 != 0)
            {
                return CountTrailingZeros(this.mVal2) + 8;
            }
            if (this.mVal3 != 0)
            {
                return CountTrailingZeros(this.mVal3) + 16;
            }
            if (this.mVal4 != 0)
            {
                return CountTrailingZeros(this.mVal4) + 24;
            }
            return this.mDigitsLen;
        }

        private static int CountTrailingZeros(uint val)
        {
            if ((val & 0xffff) == 0)
            {
                if ((val & 0xffffff) == 0)
                {
                    if ((val & 0xfffffff) == 0)
                    {
                        return 7;
                    }
                    return 6;
                }
                if ((val & 0xfffff) == 0)
                {
                    return 5;
                }
                return 4;
            }
            if ((val & 0xff) == 0)
            {
                if ((val & 0xfff) == 0)
                {
                    return 3;
                }
                return 2;
            }
            if ((val & 0xf) == 0)
            {
                return 1;
            }
            return 0;
        }

