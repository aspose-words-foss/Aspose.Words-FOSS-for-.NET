        private class CustomInfo
        {
            private readonly NumberFormattingOptions mNumberFormattingOptions;
            private readonly IStringBehaviour mStringBehaviour;

            public bool UseGroup = false;

            public int DecimalDigits = 0;

            public readonly List<IChar> DecimalDigitSources = new List<IChar>();

            public int DecimalPointPos = -1;

            public int DecimalTailSharpDigits = 0;

            public int IntegerDigits = 0;

            public readonly List<IChar> IntegerDigitSources = new List<IChar>();

            public int IntegerHeadSharpDigits = 0;

            public int IntegerHeadPos = 0;

            public int DividePlaces = 0;

            public int Percents = 0;

            public int Permilles = 0;

            public bool HasExplicitSign = false;

            private CustomInfo(NumberFormattingOptions options, IStringBehaviour stringBehaviour)
            {
                mNumberFormattingOptions = options;
                mStringBehaviour = stringBehaviour;
            }

            public static void GetActiveSection(
                string format, ref bool positive, bool zero, ref int offset, ref int length)
            {
                int[] lens = new int[3];
                int index = 0;
                int lastPos = 0;
                bool quoted = false;

                for (int i = 0; i < format.Length; i++)
                {
                    char c = format[i];

                    if (c == '\"' || c == '\'')
                    {
                        if (i == 0 || format[i - 1] != '\\')
                        {
                            quoted = !quoted;
                        }

                        continue;
                    }

                    if (c == ';' && !quoted && (i == 0 || format[i - 1] != '\\'))
                    {
                        lens[index++] = i - lastPos;
                        lastPos = i + 1;
                        if (index == 3)
                        {
                            break;
                        }
                    }
                }

                if (index == 0)
                {
                    offset = 0;
                    length = format.Length;
                    return;
                }
                if (index == 1)
                {
                    if (positive || zero)
                    {
                        offset = 0;
                        length = lens[0];
                        return;
                    }
                    if (lens[0] + 1 < format.Length)
                    {
                        positive = true;
                        offset = lens[0] + 1;
                        length = format.Length - offset;
                        return;
                    }
                    offset = 0;
                    length = lens[0];
                    return;
                }
                if (index == 2)
                {
                    if (zero)
                    {
                        offset = lens[0] + lens[1] + 2;
                        length = format.Length - offset;
                        return;
                    }
                    if (positive)
                    {
                        offset = 0;
                        length = lens[0];
                        return;
                    }
                    if (lens[1] > 0)
                    {
                        positive = true;
                        offset = lens[0] + 1;
                        length = lens[1];
                        return;
                    }
                    offset = 0;
                    length = lens[0];
                    return;
                }
                if (index == 3)
                {
                    if (zero)
                    {
                        offset = lens[0] + lens[1] + 2;
                        length = lens[2];
                        return;
                    }
                    if (positive)
                    {
                        offset = 0;
                        length = lens[0];
                        return;
                    }
                    if (lens[1] > 0)
                    {
                        positive = true;
                        offset = lens[0] + 1;
                        length = lens[1];
                        return;
                    }
                    offset = 0;
                    length = lens[0];
                    return;
                }

                throw new ArgumentException();
            }

            public static CustomInfo Parse(
                IString format,
                int offset,
                int length,
                NumberFormattingOptions options,
                IStringBehaviour stringBehaviour)
            {
                char literal = '\0';
                bool integerArea = true;
                bool decimalArea = false;
                bool sharpContinues = true;

                CustomInfo info = new CustomInfo(options, stringBehaviour);
                int groupSeparatorCounter = 0;

                for (int i = offset; i - offset < length; i++)
                {
                    char c = format[i].ToSystemChar();

                    if (c == literal && c != '\0')
                    {
                        literal = '\0';
                        continue;
                    }
                    if (literal != '\0')
                    {
                        continue;
                    }

                    switch (c)
                    {
                        case '\\':
                            i++;
                            continue;
                        case '\'':
                        case '\"':
                            literal = c;
                            continue;
                        case '#':
                        case '0':
                            if (c != '#')
                            {
                                sharpContinues = false;
                                if (decimalArea)
                                {
                                    info.DecimalTailSharpDigits = 0;
                                }
                            }
                            else // alexnosk: Code moved from case '#', to avoid using goto case '0'.
                            {
                                if (sharpContinues && integerArea)
                                {
                                    info.IntegerHeadSharpDigits++;
                                }
                                else if (decimalArea)
                                {
                                    info.DecimalTailSharpDigits++;
                                }
                            }

                            if (info.IntegerHeadPos == -1)
                            {
                                info.IntegerHeadPos = i;
                            }

                            if (integerArea)
                            {
                                info.IntegerDigits++;
                                info.IntegerDigitSources.Add(format[i]);

                            }
                            else if (decimalArea)
                            {
                                info.DecimalDigits++;
                                info.DecimalDigitSources.Add(format[i]);
                            }

                            if (groupSeparatorCounter > 0)
                            {
                                info.UseGroup = true;
                            }
                            groupSeparatorCounter = 0;

                            break;

                        case '.':
                            integerArea = false;
                            decimalArea = true;

                            if (info.DecimalPointPos == -1)
                            {
                                info.DecimalPointPos = i;
                            }
                            break;
                        case '%':
                            info.Percents++;
                            break;
                        case '\u2030':
                            info.Permilles++;
                            break;
                        case ',':
                            groupSeparatorCounter++;
                            break;
                        case '+':
                        case '-':
                            info.HasExplicitSign = true;
                            break;
                    }
                }

                if (info.DecimalDigits == 0)
                {
                    info.DecimalPointPos = -1;
                }

                info.DividePlaces += groupSeparatorCounter * 3;

                return info;
            }

            internal IString Format(
                IString format,
                int offset,
                int length,
                NumberFormatInfo nfi,
                bool positive,
                bool isZero,
                IStringBuilder sbInt,
                IStringBuilder sbDec,
                double value)
            {
                IStringBuilder sb = mStringBehaviour.CreateBuilder();
                char literal = '\0';
                bool integerArea = true;
                int intSharpCounter = 0;
                int sbIntIndex = 0;
                int sbDecIndex = 0;
                bool addImplicitSign = !positive && !HasExplicitSign;

                int[] groups = nfi.NumberGroupSizes;
                string groupSeparator = nfi.NumberGroupSeparator;
                int intLen = 0, total = 0, groupIndex = 0, counter = 0, groupSize = 0;
                if (UseGroup && groups.Length > 0)
                {
                    intLen = sbInt.Length;
                    for (int i = 0; i < groups.Length; i++)
                    {
                        total += groups[i];
                        if (total <= intLen)
                        {
                            groupIndex = i;
                        }
                    }
                    groupSize = groups[groupIndex];
                    int fraction = intLen > total ? intLen - total : 0;
                    if (groupSize == 0)
                    {
                        while (groupIndex >= 0 && groups[groupIndex] == 0)
                        {
                            groupIndex--;
                        }

                        groupSize = fraction > 0 ? fraction : groups[groupIndex];
                    }
                    if (fraction == 0)
                    {
                        counter = groupSize;
                    }
                    else
                    {
                        groupIndex += fraction / groupSize;
                        counter = fraction % groupSize;
                        if (counter == 0)
                        {
                            counter = groupSize;
                        }
                        else
                        {
                            groupIndex++;
                        }
                    }
                }
                else
                {
                    UseGroup = false;
                }

                for (int i = offset; i - offset < length; i++)
                {
                    IChar c = format[i];
                    char systemChar = c.ToSystemChar();

                    if (systemChar == literal && systemChar != '\0')
                    {
                        literal = '\0';
                        continue;
                    }
                    if (literal != '\0')
                    {
                        sb.Append(c);
                        continue;
                    }

                    switch (systemChar)
                    {
                        case '\\':
                            i++;
                            if (i - offset < length)
                            {
                                sb.Append(format[i]);
                            }
                            continue;
                        case '\'':
                        case '\"':
                            literal = systemChar;
                            continue;
                        case '#':
                            // WORDSNET-8614 replace # with white space, when no digit
                            if (integerArea)
                            {
                                if (addImplicitSign)
                                {
                                    sb.Append(nfi.NegativeSign, c);
                                    addImplicitSign = false;
                                }

                                intSharpCounter++;
                                if (IntegerDigits - intSharpCounter < sbInt.Length + sbIntIndex || systemChar == '0')
                                {
                                    while (IntegerDigits - intSharpCounter + sbIntIndex < sbInt.Length)
                                    {
                                        sb.Append(sbInt[sbIntIndex++]);
                                        if (UseGroup && --intLen > 0 && --counter == 0)
                                        {
                                            sb.Append(groupSeparator, c);
                                            if (--groupIndex < groups.Length && groupIndex >= 0)
                                            {
                                                groupSize = groups[groupIndex];
                                            }
                                            counter = groupSize;
                                        }
                                    }
                                }
                                else if (!NumberFormattingOptionsUtil.HasIgnoreUnmatchedDigitPlaceholder(mNumberFormattingOptions))
                                {
                                    sb.Append(' ', c);
                                }

                                break;
                            }

                            bool onlyZerosLeft = true;
                            for (int j = sbDecIndex; j < sbDec.Length; j++)
                            {
                                if (sbDec[j].ToSystemChar() != '0')
                                {
                                    onlyZerosLeft = false;
                                    break;
                                }
                            }

                            if (!onlyZerosLeft)
                            {
                                sb.Append(sbDec[sbDecIndex++]);
                            }
                            else if (isZero && sbDecIndex == 0)
                            {
                                sbDecIndex++;
                                sb.Append('0', c);
                            }
                            else
                            {
                                sb.Append(' ', c);
                            }

                            break;

                        case '0':
                            if (integerArea)
                            {
                                if (addImplicitSign)
                                {
                                    sb.Append(nfi.NegativeSign, c);
                                    addImplicitSign = false;
                                }

                                intSharpCounter++;
                                if (IntegerDigits - intSharpCounter < sbInt.Length + sbIntIndex || systemChar == '0')
                                {
                                    while (IntegerDigits - intSharpCounter + sbIntIndex < sbInt.Length)
                                    {
                                        sb.Append(sbInt[sbIntIndex++]);
                                        if (UseGroup && --intLen > 0 && --counter == 0)
                                        {
                                            sb.Append(groupSeparator, c);
                                            if (--groupIndex < groups.Length && groupIndex >= 0)
                                            {
                                                groupSize = groups[groupIndex];
                                            }
                                            counter = groupSize;
                                        }
                                    }
                                }
                                break;
                            }

                            if (sbDecIndex < sbDec.Length)
                            {
                                sb.Append(sbDec[sbDecIndex++]);
                            }

                            break;

                        case '.':
                            if (DecimalPointPos == i)
                            {
                                if (DecimalDigits > 0)
                                {
                                    while (sbIntIndex < sbInt.Length)
                                        sb.Append(sbInt[sbIntIndex++]);
                                }
                            }

                            sb.Append(nfi.NumberDecimalSeparator, c);
                            integerArea = false;
                            break;
                        case ',':
                            break;
                        case '%':
                            sb.Append(nfi.PercentSymbol, c);
                            break;
                        case '\u2030':
                            sb.Append(nfi.PerMilleSymbol, c);
                            break;
                        case '+':
                            // Adds a plus sign to a positive result, a minus sign to a negative result, or a space if the result is 0 (zero).
                            if (value > 0)
                            {
                                sb.Append('+', c);
                            }
                            else if (value < 0)
                            {
                                sb.Append('-', c);
                            }
                            else
                            {
                                sb.Append(' ', c);
                            }
                            break;
                        case '-':
                            // Adds a minus sign to a negative result, or adds a space if the result is positive or 0 (zero).
                            // Minus will be added depending on positive flag
                            if (value >= 0)
                            {
                                sb.Append(' ', c);
                            }
                            else
                            {
                                sb.Append('-', c);
                            }
                            break;
                        default:
                            sb.Append(c);
                            break;
                    }
                }

                return sb.ToIString();
            }
        }

