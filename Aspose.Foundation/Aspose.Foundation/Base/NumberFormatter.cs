            return 0;
        }

        // Helper to count the 10th scale (number of digits) in a number
        private static int ScaleOrder(long hi)
        {
            for (int i = TenPowersListLength - 1; i >= 0; i--)
            {
                if (hi >= GetTenPowerOf(i))
                {
                    return i + 1;
                }
            }
            return 1;
        }

        // Compute the initial precision for rounding a floating number
        // according to the used format.
        private int InitialFloatingPrecision()
        {
            if (this.mSpecifier == 'R')
            {
                return this.mDefPrecision + 2;
            }
            if (this.mPrecision < this.mDefPrecision)
            {
                return this.mDefPrecision;
            }
            if (this.mSpecifier == 'G')
            {
                return Math.Min(this.mDefPrecision + 2, this.mPrecision);
            }
            if (this.mSpecifier == 'E')
            {
                return Math.Min(this.mDefPrecision + 2, this.mPrecision + 1);
            }
            return this.mDefPrecision;
        }

