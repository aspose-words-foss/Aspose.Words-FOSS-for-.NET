    /// <summary>
    /// Generates "property bags" classes that represent property collections in a flat way.
    /// </summary>
    [TestFixture]
    [JavaDelete("Not porting to Java.")]
    public class PrBagGenerator
    {
        [Test, Ignore("PrBagGenerator")]
        public void TestGenerate()
        {
            Execute(
                @"X:\awnet\Aspose.Words\Model\Drawing\ShapeAttr.cs", 
                "ShapePrBag",
                "ShapePr",
                "Aspose.Words.Drawing");

        }

        private static void Execute(
            string srcFileName, 
            string prBagClassName, 
            string attrCollectionClassName, 
            string namespaceName)
        {
            string dstDir = Path.GetDirectoryName(srcFileName);
            string dstFileName = Path.Combine(dstDir, prBagClassName + ".cs");
            using (StreamWriter writer = File.CreateText(dstFileName))
            {
                writer.WriteLine("// DO NOT MODIFY. THIS FILE IS AUTOGENERATED FROM");
                writer.WriteLine("// {0}", srcFileName);
                writer.WriteLine();
                writer.WriteLine("using System;");
                writer.WriteLine();
                writer.WriteLine("namespace {0}", namespaceName);
                writer.WriteLine("{");

                writer.WriteLine("\tinternal class {0}", prBagClassName);
                writer.WriteLine("\t{");

                List<string> attrNames = CollectAttrNames(srcFileName);

                // Write fields.
                foreach (string attrName in attrNames)
                    writer.WriteLine("\t\tinternal object {0};", attrName);

                // Write Reset.
                writer.WriteLine();
                writer.WriteLine("\t\tinternal void Reset()");
                writer.WriteLine("\t\t{");
                foreach (string attrName in attrNames)
                    writer.WriteLine("\t\t\t{0} = null;", attrName);
                writer.WriteLine("\t\t}");  // reset

                // Write Fill.
                writer.WriteLine();
                string prVarName = attrCollectionClassName.ToLower();
                writer.WriteLine("\t\tinternal void Fill({0} {1})", attrCollectionClassName, prVarName);
                writer.WriteLine("\t\t{");

                writer.WriteLine("\t\t\tint count = {0}.Count;", prVarName);
                writer.WriteLine("\t\t\tfor (int i = 0; i < count; i++)");
                writer.WriteLine("\t\t\t{");
                writer.WriteLine("\t\t\t\tint key = {0}.GetKey(i);", prVarName);
                writer.WriteLine("\t\t\t\tobject value = {0}.GetByIndex(i);", prVarName);
                
                writer.WriteLine("\t\t\t\tswitch (key)");
                writer.WriteLine("\t\t\t\t{");

                string enumName = Path.GetFileNameWithoutExtension(srcFileName);
                foreach (string attrName in attrNames)
                {
                    writer.WriteLine("\t\t\t\t\tcase {0}.{1}:", enumName, attrName);
                    writer.WriteLine("\t\t\t\t\t\t{0} = value;", attrName);
                    writer.WriteLine("\t\t\t\t\t\tbreak;");
                }

                writer.WriteLine("\t\t\t\t\tdefault:");
                writer.WriteLine("\t\t\t\t\t\tthrow new InvalidOperationException(\"Unknown attribute.\");");
                writer.WriteLine("\t\t\t\t}");  // switch

                writer.WriteLine("\t\t\t}");  // loop

                writer.WriteLine("\t\t}");  // fill

                writer.WriteLine("\t}");    // class
                writer.WriteLine("}");  // namespace
            }
        }

        private static List<string> CollectAttrNames(string srcFileName)
        {
            List<string> result = new List<string>();
            using (StreamReader reader = File.OpenText(srcFileName))
            {
                while (true)
                {
                    string srcLine = reader.ReadLine();
                    if (srcLine == null)
                        break;

                    Match match = gAttrLineRegex.Match(srcLine);
                    if (match.Success)
                    {
                        string attrName = match.Groups[1].Value;
                        result.Add(attrName);
                    }
                }
            }
            return result;
        }

        /// <summary>
        /// To parse lines of code such as this:
        /// 
        /// internal const int LockRotation = 119;
        /// </summary>
        private static readonly Regex gAttrLineRegex = new Regex(@"^\s*internal\s+const\s+int\s+(\S+)");
    }
}
