        /// <summary>
        /// Verify header/footer IsLinkedToPrevious == true.
        /// </summary>
        private static void VerifyHeaderFooterIsLinkedToPrevious(Document doc)
        {
            HeaderFooterCollection hfc = doc.Sections[1].HeadersFooters;

            for (int i = (int)HeaderFooterType.HeaderEven;
                (i <= (int)HeaderFooterType.FooterFirst) && (hfc[(HeaderFooterType)i] != null);
                i++)
                Assert.That(hfc[HeaderFooterType.HeaderPrimary].IsLinkedToPrevious, Is.True);
        }



