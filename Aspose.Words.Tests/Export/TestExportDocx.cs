        /// <summary>
        /// WORDSNET-5726 Specified Alpha color component in Shape.FillColor exported incorrectly to DOCX.
        /// Alpha color component in Shape.Fill.Color should be converted to Shape.Fill.Opacity.
        /// </summary>
        [Test]
        public void TestJira5726()
        {
            Document doc = new Document();

            // Add transparent red shape.
            Shape transparentShape = AddShape(doc, Color.FromArgb(0, 255, 0, 0));
            Assert.That(MathUtil.IsZero(transparentShape.Fill.Opacity), Is.True);

            // Add opaque red shape
            Shape opaqueShape = AddShape(doc, Color.FromArgb(255, 255, 0, 0));
            Assert.That(opaqueShape.GetDirectShapeAttrInternal(ShapeAttr.FillOpacity), Is.Null);

            // Add semi-transparent red shape.
            Shape semitransparentShape = AddShape(doc, Color.FromArgb(128, 255, 0, 0));
            Assert.That(MathUtil.AreEqual(0.5, semitransparentShape.Fill.Opacity, 0.01), Is.True);

            TestUtil.SaveOpen(doc, @"ExportDocx\TestJira5726", UnifiedScenario.Docx2DocxNoGold);

            semitransparentShape = (Shape) doc.FirstSection.Body.FirstParagraph.FirstChild;
            Assert.That(MathUtil.AreEqual(0.5, semitransparentShape.Fill.Opacity, 0.01), Is.True);

            opaqueShape = (Shape) semitransparentShape.NextSibling;
            Assert.That(opaqueShape.GetDirectShapeAttrInternal(ShapeAttr.FillOpacity), Is.Null);

            transparentShape = (Shape)opaqueShape.NextSibling;
            Assert.That(MathUtil.IsZero(transparentShape.Fill.Opacity), Is.True);
        }



