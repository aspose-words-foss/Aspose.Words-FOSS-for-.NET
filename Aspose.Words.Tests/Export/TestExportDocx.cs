        /// <summary>
        /// Checks parameters of text box rectangles in output documents after conversion
        /// using specified scenarios.
        /// </summary>
        /// <param name="filePath">Path to source file without extension.</param>
        /// <param name="scenario">Conversion scenario.</param>
        private static void UnifiedTxbxPosition(string filePath, UnifiedScenario scenario)
        {
            Document doc = TestUtil.OpenSaveOpen(filePath, scenario | UnifiedScenario.NoGold);

            Shape shape = (Shape)doc.FirstSection.Body.GetChild(NodeType.Shape, 0, true);
            PathRectangle[] rects1 = (PathRectangle[])shape.ShapePr[ShapeAttr.GeometryPathTextBoxRects];

            shape = (Shape)doc.FirstSection.Body.GetChild(NodeType.Shape, 1, true);
            PathRectangle[] rects2 = (PathRectangle[])shape.ShapePr[ShapeAttr.GeometryPathTextBoxRects];

            switch(scenario)
            {
                case UnifiedScenario.Wml2Wml:
                case UnifiedScenario.Doc2Doc:
                case UnifiedScenario.Doc2Docx:
                case UnifiedScenario.Docx2Wml:
                case UnifiedScenario.Docx2Docx:
                {
                    Assert.That((rects1 == null) || (rects1.Length == 0), Is.True);
                    Assert.That((rects2 == null) || (rects2.Length == 0), Is.True);
                    break;
                }
                default:
                {
                    Assert.That(rects1.Length, Is.EqualTo(1));
                    Assert.That(rects2.Length, Is.EqualTo(1));
                    ChecksPathRectangle(rects1[0], 0, 0, 1000, 1000);
                    ChecksPathRectangle(rects2[0], 0, 0, 21600, 21600);
                    break;
                }
            }
        }

