        /// Create document with image.
        /// </summary>
        /// <param name="title">Value to be set for Title attribute of ImagweData element.</param>
        /// <param name="isLinkedOnly">When true shape in document is linked only.</param>
        /// <param name="shapeType">Type of the shape.</param>
        /// <returns></returns>
        private static Document CreateImageDocument(string title, bool isLinkedOnly, ShapeType shapeType)
        {
            string picPath = TestUtil.BuildTestFileName(@"ExportDocx\TestJira13261.gif");
            Document doc = new Document();
            DocumentBuilder builder = new DocumentBuilder(doc);
            Shape linkedOnly = new Shape(builder.Document, shapeType);
