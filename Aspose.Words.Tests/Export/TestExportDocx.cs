        {
            const string testName = "TestDefect18748";

            DocumentBuilder builder = new DocumentBuilder();
            // Create two sections.
            builder.Write("This is the first section with header.");
            builder.InsertBreak(BreakType.SectionBreakNewPage);
            builder.Write("This is the second section. It should have the same header as previous.");
            // Create header in the first section.
            builder.MoveToSection(0);
            builder.MoveToHeaderFooter(HeaderFooterType.HeaderPrimary);
            builder.Write("This is header, it should be displayed in both sections.");
