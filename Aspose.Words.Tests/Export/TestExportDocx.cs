        /// <param name="format">Format in which the document is saved.</param>
        /// <param name="exist">When True check Title attribute exist, when false check Title skipped.</param>
        private static void CheckTitleAttribute(Document doc, SaveFormat format, bool exist)
        {
            XmlNode imageData = null;

            switch(format)
            {
                case SaveFormat.Docx:
                    imageData = GetImageDataDocx(doc);
                    break;
                default:
                    Assert.Fail("Invalid format.");
                    break;
            }

            XmlAttribute atrTitle = (XmlAttribute) imageData.Attributes["o:title"];

            if (exist)
                Assert.That(atrTitle, IsNot.Null());
            else
                Assert.That(atrTitle, Is.Null);
        }

